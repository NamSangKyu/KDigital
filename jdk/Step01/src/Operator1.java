
public class Operator1 {
	/*
	 * 	정수형 변수 두개 선언하여 값은 임의로 초기화
	 * 	모든 사칙연산의 결과를 출력 
	 */
	public static void main(String[] args) {
		int n1 = 10, n2 = 6;
		
		System.out.println(n1 + n2);
		System.out.println(n1 - n2);
		System.out.println(n1 * n2);
		System.out.println(n1 / n2);
		System.out.println(n1 % n2);
		
	}

}
