using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Step01
{
    internal class VarType
    {
        static void Main(string[] args)
        {
            //var 모든 데이터 타입을 지정할 수 있는 변수 타입
            var a = 10;
            var b = "HelloWorld";
            var c = 3.1415;
            var d = true;
            var e = 'C';

            Console.WriteLine(a);
            Console.WriteLine(b);
            Console.WriteLine(c);
            Console.WriteLine(d);
            Console.WriteLine(e);


        }
        
    }
}
